    public boolean verUbicacion(String ubicacion){
    
        boolean compUbi = true;
        if (ubicacion=="Rural") {
            compUbi=false;
        }
        return compUbi;
    }
    /**
     * Calcula diferencia entre las tasas
     * @param periodo
     * @param estAct
     * @param estReal
     * @param ubicacion
     * @return diferencia entre las tasas
     */
    public double difTasas(int periodo, int estAct, int estReal, String ubicacion) {

        double diferTasas = 0;
        if (verUbicacion(ubicacion) == true) {
            diferTasas = selPeriodo(periodo, estAct) - selPeriodo(periodo, estReal);
        } else {
            diferTasas = selPeriodo(periodo, 1) - selPeriodo(periodo, 1);
        }
        return diferTasas;
    }
